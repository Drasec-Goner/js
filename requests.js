//Requests Methods

//HTTP methods define the actions that clients(such as web browsers) can perform on resources hosted on the servers

//GET: GET method is used to request data from a specified resource

//POST: POST method is used to submit data to be processed to a specified resource

//PUT: PUT method is used to update an existing resource or create a new resource if it does not exist at the specified URL

//PATCh: PATCH method is used to apply partial modifications to a resource

//DELETE: DELETE method is used to request the removal of the specified resource

